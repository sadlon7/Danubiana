			<p>Aktuálna samostatná výstava renomovaného európskeho umelca Jiřího Georga Dokoupila (1954) pod názvom Oxymoron prináša na Slovensko nielen ikonické bublinové obrazy, ale aj jeden z jeho najnovších projektov Furniture For Fotos. Dokoupil vstúpil na svetovú scénu v osemdesiatych rokoch minulého storočia v rámci silnej nemeckej neoexpresívnej maliarskej školy. V priebehu rokov postavil osobitý maliarsky program, ktorý modeluje polemiku so samotným médiom maľby. Ide o posun od expresívnej figurálnej maľby k novým experimentálnym a „mentálnym“ polohám maliarskej práce. V priebehu rokov priniesol takmer stovku inovatívnych maliarskych techník a štýlov, ktoré podáva v ucelených cykloch (maľba pneumatikou, bičom, sadzami, dymom sviečky, „filmové“ plátna a pod.). 
				<br><br>
				Na súčasnej výstave prezentuje sériu malieb s mydlovými bublinami, ktoré rozvíja od roku 1992 hlavne vo svojom berlínskom ateliéri. Bubliny v klasickej ikonografii boli symbolom prchavosti chvíle a pominuteľnosti života, dnes možno viac svedčia o povrchnosti médií a vnímania sveta. Ide o proces kalkulovanej spontánnosti a akčnosti maľby – kde bubliny s pridaním pigmentu vytvárajú podivné organické a dynamické štruktúry, melodicky sa vznášajúce na obrazovej ploche. Je to abstraktná maľba? Materiálová maľba reálnymi bublinami? Či skôr Dokoupilov postoj voči maľbe, jeho dialóg so samotným jazykom závesného obrazu.
				V podobnom duchu Jiří Georg Dokoupil vytvoril aj sériu špeciálnych objektov, ktoré stierajú rozdiely medzi druhou a treťou dimenziou. Ide o cyklus Furniture For Fotos, ktorý po premiére v pražskej DSC Gallery predstaví prvýkrát aj na Slovensku. „Už před sedmi lety jsem měl sen o tom, že se druhá dimenze rozhodla být třetí, ale nějak jí to nevyšlo… jde o nejautentičtější důkaz teorie Marcela Duchampa, který říká, že umělecké dílo začne existovat teprve s divákem. Objekty samotné neznamenají nic, dokud se divák sám nestane součástí sochy,“ vysvetľuje princíp tejto série samotný autor. Vznikla ucelená kolekcia elementárnych nábytkových prvkov a predmetov s iluzívnou perspektívnou skratkou, ktoré narúšajú bežné vnímanie týchto podivných objektov. Tak obe polohy Dokoupila na tejto výstave – séria malieb a 3D realizácie prinášajú príbuzné protichodné (oxymoronové) prístupy autora v jeho súčasnom umeleckom prejave. 
				<br><br>
				Jiří Georg Dokoupil sa narodil roku 1954 v Krnove. V roku 1968 sa jeho rodina presťahovala do Kolína nad Rýnom v Nemecku, kde v sedemdesiatych rokoch študoval na Akadémii výtvarných umení. Ďalej navštevoval výtvarné ateliéry na University of Frankfurt v Nemecku a Cooper Union v New Yorku, kde študoval pod vedením nemeckého konceptualistu Hansa Haackeho, ktorý mal významný vplyv na jeho ranú tvorbu. Od roku 1979 bol Dokoupil prominentným členom nemeckej neoexpresionistickej skupiny Mülheimer Freiheit, ktorú založil spolu s ďalšími piatimi umelcami. Po účasti na významnej svetovej výstave Documenta 7 v Kasseli v roku 1982 sa z neho stala hviezda svetového formátu. Odvtedy prezentoval svoju tvorbu na svetových výstavách, napríklad na Benátskom bienále (1987, 1993), vystavoval v galérii slávneho Lea Castelliho, Ileany Sonnabendovej alebo Davida Zwirnera v New Yorku, aj v umeleckých inštitúciách, ako Centre Georges Pompidou v Paríži či viedenské Museum Moderner Kunst Stiftung Ludwig (Mumok), Museo Reina Sofia v Madride alebo Museum of Modern Art (MoMA) v New Yorku. Jeho obrazy sú zastúpené v zbierkach významných umeleckých inštitúcií, i v popredných privátnych zbierkach po celom svete. V rokoch 1983 – 1984 pôsobil Dokoupil ako hosťujúci profesor na Kunstakademie v Düsseldorfe, v roku 1989 v Circulo de Ballas Artes v Madride a na Gesamtschule Kassel v rokoch 1994 – 1995. V roku 2012 získal cenu Lovis-Corinth Price. V súčasnosti žije a pracuje v Berlíne, Madride, Riu de Janeiro a v Prahe. 

				
